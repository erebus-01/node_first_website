const mongoose = require('mongoose');

const CustomerSchema = new mongoose.Schema({
  fName: {
    type: String,
    required: true
  },
  lName: {
    type: String,
    required: true
  },
  email: {
    type: String,
    required: true
  },
  password: {
    type: String,
    required: true
  },
  date: {
    type: Date,
    default: Date.now
  },
})

const Customer = mongoose.model('Customer', CustomerSchema);

module.exports = Customer